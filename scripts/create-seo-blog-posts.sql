<h2>Key Metrics for Receivables Management</h2>
<p>To manage receivables effectively, you must track key performance indicators:</p>

<h3>Days Sales Outstanding (DSO)</h3>
<p>DSO measures the average number of days it takes to collect payment after a sale. Lower DSO indicates faster collections. Calculate DSO by dividing accounts receivable by average daily sales.</p>
<p><strong>Industry Benchmarks:</strong> DSO varies by industry, but generally, DSO under 45 days is considered good for most businesses.</p>

<h3>Aging Analysis</h3>
<p>An aging report categorizes receivables by how long they''ve been outstanding:</p>
<ul>
<li>0-30 days: Current</li>
<li>31-60 days: Slightly past due</li>
<li>61-90 days: Moderately past due</li>
<li>Over 90 days: Seriously past due (high risk)</li>
</ul>
<p>Regular aging analysis helps identify collection priorities and potential bad debt.</p>

<h3>Collection Effectiveness Index (CEI)</h3>
<p>CEI measures how effectively you''re collecting receivables. A CEI above 80% is considered good. Calculate CEI by comparing beginning receivables plus sales to ending receivables.</p>

